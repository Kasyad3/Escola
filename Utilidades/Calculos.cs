namespace EscolaExemplo.Utilidades
{
    public class Calculos
    {

        public double CalculoCola(double preco, double quantidade)
        {
            return preco * quantidade;
        }
        public double CalculoPapel(double preco, double quantidade)
        {
            return preco * quantidade;
        }
        public double CalculoTesoura(double preco, double quantidade)
        {
            return preco * quantidade;
        }

    }
}
